package com.elecciones.backend.comisaria;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.*;

import java.util.List;

@RestController
@RequestMapping("/api/comisarias")
public class ComisariaController {

    @Autowired
    private ComisariaService comisariaService;

    @GetMapping
    public List<Comisaria> getAll() {
        return comisariaService.getAllComisarias();
    }
}