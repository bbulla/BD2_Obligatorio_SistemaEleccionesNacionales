package com.elecciones.backend.candidato;

public class Candidato {
    private String cc;
    private int nroPosicion;
    private String rol;

    public Candidato() {}

    public Candidato(String cc, int nroPosicion, String rol) {
        this.cc = cc;
        this.nroPosicion = nroPosicion;
        this.rol = rol;
    }

    public String getCc() {
        return cc;
    }

    public void setCc(String cc) {
        this.cc = cc;
    }

    public int getNroPosicion() {
        return nroPosicion;
    }

    public void setNroPosicion(int nroPosicion) {
        this.nroPosicion = nroPosicion;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }
}
